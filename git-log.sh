for date in "${dates[@]}"; do
  # ファイル名を「2025年1月6日.txt」の形式で作成（先頭ゼロなし）
  year=$(date -d "$date" +%-Y)
  month=$(date -d "$date" +%-m)
  day=$(date -d "$date" +%-d)
  filename="${year}年${month}月${day}日.txt"

  echo "学習記録の投稿" > "$filename"
  git add "$filename"

  # 時間は10:00に統一（必要に応じてランダム化も可能）
  GIT_COMMITTER_DATE="${date}T10:00:00" git commit --date="${date}T10:00:00" -m "学習記録の投稿"
done
